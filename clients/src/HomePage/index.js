function emoLike(){
    let colorLike = document.getElementById('emo-like')
    if(colorLike.style.color == 'black'){
        colorLike.style.color = '#1877f2'
        return colorLike
    }
    else{
        colorLike.style.color = 'black'
    }
    return 
}
